// muscle文字列（例:「大胸筋・三角筋前部・上腕三頭筋」）を区切り文字で分割し、
// 各語の読み仮名を連結した検索用文字列を返す。読みが無い語（純カタカナ等）は無視する。
export function getMuscleReadingText(muscle: string): string {
  return muscle
    .split(/[・（）()、,]/)
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => MUSCLE_READINGS[part])
    .filter((reading): reading is string => Boolean(reading))
    .join(' ');
}
